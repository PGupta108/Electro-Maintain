// ── Helper components ──────────────────────────────────────────────────────────

function modelStatusCard(icon, name, algo, task, color, status) {
  return `
    <div style="background:var(--card);border:1px solid var(--border);border-radius:var(--r2);padding:1.1rem">
      <div style="display:flex;align-items:center;justify-content:space-between;margin-bottom:.5rem">
        <span style="font-size:1.4rem">${icon}</span>
        <span style="font-family:var(--fm);font-size:.6rem;background:rgba(34,197,94,.12);color:var(--green);border:1px solid rgba(34,197,94,.2);padding:2px 7px;border-radius:10px">${status}</span>
      </div>
      <div style="font-weight:700;font-size:.88rem;margin-bottom:2px">${name}</div>
      <div style="font-family:var(--fm);font-size:.62rem;color:${color};margin-bottom:3px">${algo}</div>
      <div style="font-size:.72rem;color:var(--muted)">${task}</div>
    </div>`;
}

function rfBarChart(rf, activeCount) {
  if (!activeCount) return `<p style="color:var(--muted);font-size:.82rem">Add reminders to see RF predictions.</p>`;
  const bars = [
    { label: 'Critical', count: rf.criticalCount||0, color: 'var(--red)' },
    { label: 'High',     count: rf.highCount||0,     color: 'var(--yellow)' },
    { label: 'Medium',   count: rf.mediumCount||0,   color: 'var(--accent3)' },
    { label: 'Low',      count: rf.lowCount||0,      color: 'var(--green)' },
  ];
  return bars.map(b => {
    const pct = activeCount ? Math.round((b.count / activeCount) * 100) : 0;
    return `
      <div style="margin-bottom:.5rem">
        <div style="display:flex;justify-content:space-between;font-size:.72rem;margin-bottom:3px">
          <span style="color:${b.color};font-weight:700">${b.label}</span>
          <span style="color:var(--text);font-family:var(--fm)">${b.count} (${pct}%)</span>
        </div>
        <div style="height:7px;background:var(--border);border-radius:4px;overflow:hidden">
          <div style="height:100%;width:${pct}%;background:${b.color};border-radius:4px;transition:width .7s ease"></div>
        </div>
      </div>`;
  }).join('');
}

function miniBar(label, value, total, color) {
  const pct = total ? Math.round((value / total) * 100) : 0;
  return `
    <div style="margin-bottom:.4rem">
      <div style="display:flex;justify-content:space-between;font-size:.7rem;margin-bottom:2px">
        <span style="color:var(--muted)">${label}</span>
        <span style="font-family:var(--fm);color:var(--text)">${value} (${pct}%)</span>
      </div>
      <div style="height:4px;background:var(--border);border-radius:2px;overflow:hidden">
        <div style="height:100%;width:${pct}%;background:${color};transition:width .6s"></div>
      </div>
    </div>`;
}

function predCard(p) {
  const conf  = Math.round((p.confidence||0)*100);
  const color = conf >= 80 ? 'var(--green)' : conf >= 60 ? 'var(--yellow)' : 'var(--red)';
  return `
    <div class="ml-card" style="background:rgba(0,229,255,.04);border-color:rgba(0,229,255,.18)">
      <div style="display:flex;justify-content:space-between;align-items:flex-start">
        <div>
          <div style="font-weight:700;font-size:.88rem">${p.title}</div>
          <div style="font-size:.72rem;color:var(--muted);margin-top:2px">${p.device}</div>
        </div>
        <div style="text-align:right;flex-shrink:0;margin-left:1rem">
          <div style="font-family:var(--fm);font-size:.78rem;color:var(--accent)">${formatDate(p.predictedDate)}</div>
          <div style="font-family:var(--fm);font-size:.62rem;color:${color}">${conf}% conf.</div>
        </div>
      </div>
      <div style="margin-top:.5rem;background:var(--border);height:3px;border-radius:2px;overflow:hidden">
        <div style="height:100%;width:${conf}%;background:${color};transition:width .6s"></div>
      </div>
    </div>`;
}

function anomalyCard(a) {
  return `
    <div class="ml-card" style="background:rgba(255,107,53,.05);border-color:rgba(255,107,53,.2)">
      <div style="display:flex;gap:.6rem">
        <span style="flex-shrink:0">⚠</span>
        <div>
          <div style="font-weight:700;font-size:.85rem">${a.title}</div>
          <div style="font-size:.7rem;color:var(--muted);margin-top:2px">${a.device?.name} — ${a.device?.brand}</div>
          ${a.mlAnomalyReason ? `<div style="font-size:.72rem;color:var(--accent2);margin-top:4px;line-height:1.5">${a.mlAnomalyReason.split(';').map(r=>`• ${r.trim()}`).join('<br>')}</div>` : ''}
        </div>
      </div>
    </div>`;
}

function priorityCard(r) {
  const score = r.mlPriorityScore || 0;
  const rfLabel = r.rfPriorityLabel;
  const dtLabel = r.dtPriorityLabel;
  const color = score >= 85 ? 'var(--red)' : score >= 70 ? 'var(--yellow)' : 'var(--accent3)';
  const agree = rfLabel && dtLabel && rfLabel === dtLabel;
  return `
    <div style="background:var(--card);border:1px solid var(--border);border-radius:var(--r2);padding:.85rem 1.1rem;display:flex;align-items:center;gap:.85rem">
      <div style="width:44px;height:44px;flex-shrink:0;position:relative">
        <svg viewBox="0 0 44 44" style="transform:rotate(-90deg)">
          <circle cx="22" cy="22" r="16" fill="none" stroke="var(--border)" stroke-width="4"/>
          <circle cx="22" cy="22" r="16" fill="none" stroke="${color}" stroke-width="4"
            stroke-dasharray="${2*Math.PI*16}"
            stroke-dashoffset="${2*Math.PI*16*(1-score/100)}"
            stroke-linecap="round"/>
        </svg>
        <div style="position:absolute;inset:0;display:flex;align-items:center;justify-content:center;font-family:var(--fm);font-size:.6rem;font-weight:700;color:${color}">${score}</div>
      </div>
      <div style="flex:1;min-width:0">
        <div style="font-weight:700;font-size:.88rem;white-space:nowrap;overflow:hidden;text-overflow:ellipsis">${r.title}</div>
        <div style="font-size:.7rem;color:var(--muted);margin-top:2px">${r.device?.name} · ${formatDate(r.dueDate)}</div>
        <div style="display:flex;gap:.4rem;margin-top:4px;flex-wrap:wrap">
          ${rfLabel ? `<span style="font-family:var(--fm);font-size:.6rem;background:rgba(167,139,250,.12);color:var(--accent3);border:1px solid rgba(167,139,250,.2);padding:1px 6px;border-radius:3px">🌲 RF: ${rfLabel}</span>` : ''}
          ${dtLabel ? `<span style="font-family:var(--fm);font-size:.6rem;background:rgba(0,229,255,.1);color:var(--accent);border:1px solid rgba(0,229,255,.2);padding:1px 6px;border-radius:3px">🌳 DT: ${dtLabel}</span>` : ''}
          ${rfLabel && dtLabel ? `<span style="font-family:var(--fm);font-size:.6rem;padding:1px 6px;border-radius:3px;${agree ? 'background:rgba(34,197,94,.1);color:var(--green);border:1px solid rgba(34,197,94,.2)' : 'background:rgba(239,68,68,.1);color:var(--red);border:1px solid rgba(239,68,68,.2)'}">${agree ? '✓ Agree' : '✗ Disagree'}</span>` : ''}
        </div>
      </div>
      <div style="font-family:var(--fm);font-size:.6rem;color:${color};text-transform:uppercase;letter-spacing:.5px;flex-shrink:0">${score>=85?'CRITICAL':score>=70?'HIGH':'ELEVATED'}</div>
    </div>`;
}

function emptyCard(msg) {
  return `<div class="empty"><div class="empty-icon" style="font-size:2rem">🤖</div><p class="empty-text">${msg}</p></div>`;
}

function formatDate(d) {
  if (!d) return '—';
  return new Date(d).toLocaleDateString('en-IN', { day:'numeric', month:'short', year:'numeric' });
}
