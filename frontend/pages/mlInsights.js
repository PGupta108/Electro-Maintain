export async function renderMLInsights(container) {
  container.innerHTML = `<div style="display:flex;justify-content:center;padding:3rem"><span class="spinner" style="width:28px;height:28px;border-width:3px"></span></div>`;
  try {
    const [insightsData, remindersData, statsData] = await Promise.all([
      get('/ml/insights'),
      get('/reminders'),
      get('/ml/model-stats').catch(() => ({ stats: null }))
    ]);
    buildMLUI(container, insightsData.insights, remindersData.reminders || [], statsData.stats);
  } catch (err) {
    container.innerHTML = `<div class="empty"><div class="empty-icon">🤖</div><p class="empty-text">${err.message}</p></div>`;
  }
}

